"""
Єдина точка обчислення "сьогодні" з урахуванням часового поясу.

Проблема, яку це вирішує: голий `date.today()` бере системний час
контейнера. На більшості безкоштовних хостингів (у т.ч. Fly.io)
контейнер за замовчуванням працює в UTC, а не в Europe/Warsaw.
Тому в певні години доби "сьогодні" за UTC і "сьогодні" за Варшавою —
різні дати, і бот плутається, за яку дату шукати /done.

Усюди, де потрібна поточна календарна дата для бізнес-логіки
(відмітка /done, вечірній статус, перевірка вихідного дня тощо),
треба використовувати саме цю функцію, а не date.today() напряму.
"""

from __future__ import annotations

from datetime import date, datetime
from zoneinfo import ZoneInfo


def local_today(timezone: str) -> date:
    """Повертає поточну календарну дату за вказаним часовим поясом."""
    return datetime.now(ZoneInfo(timezone)).date()
